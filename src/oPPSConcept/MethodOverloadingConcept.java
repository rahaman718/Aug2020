package oPPSConcept;

public class MethodOverloadingConcept {

	public static void main(String[] args) {



	}
	
	
	public void load1(){
		
		System.out.println("First method ");
	}
	
	public void load1(int a){
		
		System.out.println("First method ");
	}

	public void load1(int a, int b){
		
		System.out.println("First method ");
	}
	public void load1(int a, String b){
		
		System.out.println("First method ");
	}

}
